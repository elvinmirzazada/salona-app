// Get language preference with user-specific fallback
const getSavedLanguage = (): string => {
  // First, check if there's a user-specific language saved
  // This is set when user logs in and contains userLanguage_<userId>
  const storageKeys = Object.keys(localStorage);
  const userLanguageKey = storageKeys.find(key => key.startsWith('userLanguage_'));

  if (userLanguageKey) {
    const userLang = localStorage.getItem(userLanguageKey);
    if (userLang) return userLang;
  }

  // Fall back to general app language
  const appLanguage = localStorage.getItem('appLanguage');
  if (appLanguage) return appLanguage;

  // Fallback to booking language (for backwards compatibility)
  const bookingLanguage = localStorage.getItem('bookingLanguage');
  if (bookingLanguage) return bookingLanguage;

  // Default to Estonian
  return 'ee';
};

